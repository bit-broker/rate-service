# Syntax build-docker.sh [-i|--image imagename]

# Global vars
######################################################################
PROJECT=rate-service
DOCKER_IMAGE=${PROJECT}:latest
######################################################################

# Get command
while [[ $# -gt 0 ]]
do
    key="${1}"

    case ${key} in
    -i|--image)
        DOCKER_IMAGE="${2}"
        shift;shift
        ;;
    -h|--help)
        less README.md
        exit 0
        ;;
    *) # unknown
        echo Unknown Parameter $1
        exit 4
    esac
done

echo BUILDING DOCKER ${DOCKER_IMAGE}

docker build --no-cache \
    -t ${DOCKER_IMAGE} \
    -f Dockerfile \
    .
